//Q.1} Check whether a number is even or odd using if...else statement ?
package Ifelse;

public class EvenOdd {

	public static void main(String[] args) {
		int a = 23 ;
		if (a%2==0)  // Condition for Odd --> if (a%2!==0)
		{
			System.out.println("It is a Even no :"+a);	
		} 
		
		else 
		{
			System.out.println("It is Odd no.:"+a);
		}
		

	}

}
