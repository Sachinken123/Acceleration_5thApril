//Q.4} Compute Quotient and Remainder ?
package javaprograms;

public class Quo_Rem {

	public static void main(String[] args) {
		//Remainder  & Quotient
		double a = 134.25 ;
		float b = 6.12f ;
		double c = a % b ;
		double d = a / b ;
		
		System.out.println("Remainder will be :"+c);
		System.out.println("Quotient will be :"+d);

	}

}
