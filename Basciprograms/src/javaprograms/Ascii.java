//Q.3} Find ASCII value of a character
package javaprograms;

public class Ascii {

	public static void main(String[] args) {
		for (int i = 'a'; i <= 'z'; i++) {
			System.out.println("ASCII values :"+i);
			
			
		}
		System.out.println("*****************************");
		for (int i = 'A'; i <= 'Z'; i++) {
			System.out.println("ASCII values :"+i);
			
		}
	   
	
	}

}
